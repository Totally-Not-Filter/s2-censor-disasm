; ---------------------------------------------------------------------------
; Align and pad
; input: length to align to, value to use as padding
; ---------------------------------------------------------------------------

align:	macros
	dcb.b (\1-(*%\1))%\1,0

; ---------------------------------------------------------------------------
; start the Z80
; ---------------------------------------------------------------------------
startZ80:	macros
		move.w	#0,($A11100).l

; ---------------------------------------------------------------------------
; stop the Z80
; ---------------------------------------------------------------------------
stopZ80:	macro
		move.w	#$100,($A11100).l
@wait:	btst	#0,($A11100).l
		bne.s	@wait
		endm